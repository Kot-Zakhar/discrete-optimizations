from collections import namedtuple
Item = namedtuple("Item", ['value', 'weight'])
TakenItem = namedtuple("TakenItem", ['value', 'itemIndex'])

def solve_it_dumm(capacity: int, items: list[Item]):
    value = 0
    weight = 0
    taken = [0]*len(items)


    # simple solution of taking most valuable items from top to bottom
    valuePerWeight = [(int(v) / int(w), i) for i,(v, w) in enumerate(items)]
    valuePerWeight.sort(key = lambda tup: tup[0], reverse = True)
    for vpw in valuePerWeight:
        itemIndex = vpw[1]
        item = items[vpw[1]]
        if weight + item[1] <= capacity:
            taken[itemIndex] = 1
            value += item[0]
            weight += item[1]
    
    return (value, taken)


def solve_it_dp(capacity: int, items: list[Item]):

    value = 0
    taken = [0]*len(items)

    arrOfWeights = [TakenItem(0, -1)]*(capacity+1)
    
    def ItemIncludedInWeight(weight, itemIndex):
        while weight > 0 and arrOfWeights[weight].itemIndex > -1:
            if arrOfWeights[weight].itemIndex == itemIndex:
                return True
            weight -= items[arrOfWeights[weight].itemIndex].weight
        return False

    for w in range(1, capacity + 1):
        newItem = arrOfWeights[w - 1]

        for index, item in enumerate(items):
            if item.weight > w:
                continue
            prevItem = arrOfWeights[w - item.weight]
            if prevItem.value + item.value > newItem.value and not ItemIncludedInWeight(w - item.weight, index):
                newItem = TakenItem(prevItem.value + item.value, index)

        arrOfWeights[w] = newItem


    value = arrOfWeights[capacity].value
    weightIndex = capacity
    while weightIndex > 0 and arrOfWeights[weightIndex].itemIndex > -1:
        taken[arrOfWeights[weightIndex].itemIndex] = 1
        weightIndex -= items[arrOfWeights[weightIndex].itemIndex].weight
        
    return (value, taken)
    
