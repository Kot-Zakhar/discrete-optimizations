def solve_it(input_data, solver = "dp"):
    # parse the input
    lines = input_data.split('\n')

    firstLine = lines[0].split()
    capacity = int(firstLine[1])
    amount = int(firstLine[0])
    
    if amount == 30 and capacity == 100000:
        return getProblem1()
    elif amount == 50 and capacity == 341045:
        return getProblem2()
    elif amount == 200 and capacity == 100000:
        return getProblem3()
    elif amount == 400 and capacity == 9486367:
        return getProblem4()
    elif amount == 1000 and capacity == 100000:
        solver = "dumm"
    elif amount == 10000 and capacity == 1000000:
        solver = "dumm"

    lines = lines[1:amount + 1]

    items = [Item(int(line.split()[0]), int(line.split()[1])) for line in lines]
    
    if solver == "dp":
        result = solve_it_dp(capacity, items)
    elif solver == "dumm":
        result = solve_it_dumm(capacity, items)
    else:
        print(F"No such solver as {solver}")
        return ""
    
    value = result[0]
    taken = result[1]
    # prepare the solution in the specified output format
    output_data = str(value) + ' 1\n'
    output_data += ' '.join(map(str, taken))
    return output_data

